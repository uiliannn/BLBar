    private void jbGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGerarRelatorioActionPerformed
        ControllerVendas controllerVendas = new ControllerVendas();
        BLDatas bl = new BLDatas();
        Date dataInicial = null, dataFinal = null;
        int codigoCliente = 0;
        codigoCliente = Integer.parseInt(cbCodCliente.getSelectedItem().toString());
        try {
            dataInicial = bl.converterDataParaDateUS(bl.converterDataParaDateUS(jdcDataInicial.getDate()));
            dataFinal = bl.converterDataParaDateUS(bl.converterDataParaDateUS(jdcDataFinal.getDate()));
        } catch (Exception ex) {
            Logger.getLogger(ViewVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbClientes.getSelectedItem().toString().equals("TODOS OS CLIENTES")) {
            controllerVendas.gerarRelatorioVendaTodosCliente(dataInicial, dataFinal);
        } else {
            controllerVendas.gerarRelatorioVendasCliente(dataInicial, dataFinal, codigoCliente);
        }
    }//GEN-LAST:event_jbGerarRelatorioActionPerformed

